import Foundation
import CoreGraphics

public extension Double {
    
    /// Int.
    var int: Int {
        return Int(self)
    }
    
    /// Float.
    var float: Float {
        return Float(self)
    }
    
    /// CGFloat.
    var cgFloat: CGFloat {
        return CGFloat(self)
    }
    
}
